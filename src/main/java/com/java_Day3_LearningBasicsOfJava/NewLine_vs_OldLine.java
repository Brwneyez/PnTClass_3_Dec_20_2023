package com.java_Day3_LearningBasicsOfJava;

public class NewLine_vs_OldLine {

	public static void main(String[] args) {
		// 

		
		int i = 10;
		int j = 20;
		int k = 30; 
		
		System.out.println(i); // ln means go to a new line
		System.out.println(j);
		System.out.println(k);
		
		int m = 10;
		int n = 20;
		int o = 30; 
		
		System.out.print(m);  // removal of the ln means it will stay on same line
		System.out.print(n);
		System.out.print(o);
	}

}
